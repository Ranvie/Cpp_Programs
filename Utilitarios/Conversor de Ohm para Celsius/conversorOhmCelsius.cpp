float convOhmCelsius(Tabela tab[TAM], float iOhm)
{
	
	int i;
	float fCelsius;
	
	//Encontra o intervalo;
	for(i = 0; i < TAM && iOhm < tab[i].fResistencia; i++);

	//Converte;
	fCelsius = tab[i-1].iCelsius + (tab[i].iCelsius - tab[i-1].iCelsius)*(iOhm - tab[i-1].fResistencia) / ( tab[i].fResistencia - tab[i-1].fResistencia );
	
	return fCelsius;
	
}

